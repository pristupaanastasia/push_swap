#include "libft.h"

void	ft_bzero(void *src, size_t n)
{
	unsigned char	*dst;
	int				i;

	i = 0;
	dst = src;
	while (n != 0)
	{
		dst[i] = 0;
		i++;
		n--;
	}
}
